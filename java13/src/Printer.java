public class Printer
{
	static void println(int result)
	{
		System.out.println(result);
	}
	
	static void println(boolean result)
	{
		System.out.println(result);
	}
	
	static void println(double result)
	{
		System.out.println(result);
	}
	
	static void println(String result)
	{
		System.out.println(result);
	}
}
